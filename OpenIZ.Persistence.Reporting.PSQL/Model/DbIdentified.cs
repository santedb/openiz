using System;
using OpenIZ.OrmLite;
using OpenIZ.OrmLite.Attributes;

namespace OpenIZ.Persistence.Reporting.PSQL.Model
{
	/// <summary>
	/// Represents identified data.
	/// </summary>
	/// <seealso cref="OpenIZ.OrmLite.IAdoLoadedData" />
	/// <seealso cref="IDbIdentified" />
	public abstract class DbIdentified : IAdoLoadedData, IDbIdentified
	{
		/// <summary>
		/// Initializes a new instance of the <see cref="DbIdentified"/> class.
		/// </summary>
		/// <param name="key">The key.</param>
		protected DbIdentified(Guid key)
		{
			this.Key = key;
		}

		/// <summary>
		/// Gets the data context
		/// </summary>
		/// <value>The context.</value>
		public DataContext Context { get; set; }

		/// <summary>
		/// Gets or sets the key.
		/// </summary>
		/// <value>The key.</value>
		[AutoGenerated]
		public abstract Guid Key { get; set; }
	}
}
